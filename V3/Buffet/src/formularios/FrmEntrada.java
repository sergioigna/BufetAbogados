    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String nombre, contrasena;
        
        nombre = txtUsuario.getText();
        contrasena = txtContrasena.getText();
        
        estUsuario p = cab;
        int sw = 0;
        
        while (p != null || sw == 0){            
            if (nombre.equals(p.usuario) && contrasena.equals(p.contrasena)){
                switch(p.perfil){
                    case "Administrador":
                        FrmAdministrador  formulario = new FrmAdministrador();
                        formulario.show();
                        break;
                    case "Tecnico":
                        FrmAdministrador_1  formularioDos = new FrmAdministrador_1(p);
                        formularioDos.show();
                        break;
                }
                this.setVisible(false);   
                sw = 1;
                usuarioLogin.texto = p.usuario;
                
            }
            p = p.liga;
        }
        
        
        if (this.isVisible()){
            JOptionPane.showMessageDialog(rootPane, "Usuario o contrase単a incorrecto");    
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

