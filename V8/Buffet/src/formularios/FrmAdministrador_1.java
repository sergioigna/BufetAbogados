    private void iconCompMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_iconCompMouseClicked
      iconosGrises();
        iconComp.setIcon(new ImageIcon("src/imagenes/componentes.png"));
        
        if(estacerrado(componentes)){
            
            componentes = new IfrmCrudComponentes();
            jDesktopPane1.add(componentes);
            
            BasicInternalFrameTitlePane titlePane = (BasicInternalFrameTitlePane)((BasicInternalFrameUI) componentes.getUI()).getNorthPane();  
            componentes.remove(titlePane); 
           
            componentes.show();
        }
        else{
            try {
                componentes.setSelected(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(FrmAdministrador.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_iconCompMouseClicked

