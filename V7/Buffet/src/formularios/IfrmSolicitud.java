            +  "Activo"+","+"0"+","+"0";

            nuevoArticulo(registro);

            String miFila[] = {Lugar, fecha, horaF, horaI};
            DefaultTableModel miTabla = (DefaultTableModel) this.tbpSolicitud.getModel();
            miTabla.addRow(miFila);

