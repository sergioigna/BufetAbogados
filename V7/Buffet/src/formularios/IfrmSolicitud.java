    private void btnRegiscompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegiscompActionPerformed

        String codigo = txtcodigo1.getText();
        String acceso = cmbacceso.getToolTipText();
        String proceso = cmbPro.getToolTipText();
        String tipoExp = cmbTipo.getToolTipText();
        String nombre = txtNombre.getText();

        String tipo = "";

        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta  llenar el codigo", "Atenci贸n", JOptionPane.ERROR_MESSAGE);
            txtcodigo1.requestFocus();
        } else if (acceso.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta  llenar el campo de acceso", "Atenci贸n", JOptionPane.ERROR_MESSAGE);
            cmbacceso.requestFocus();
        } else if (proceso.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta  llenar el campo de modelo", "Atenci贸n", JOptionPane.ERROR_MESSAGE);
            cmbTipo.requestFocus();
        } else if (tipo.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un tipo de equipo", "Atenci贸n", JOptionPane.ERROR_MESSAGE);
        } else {
            String registro = "---" + codigo + "," + tipo + "," + proceso + "," + acceso
            + "," + fecha() + "," + "Activo" + "," + "2";

            nuevoRegistro(registro, "E:/componentes.in");

            estComponente p = new estComponente();

            p.codigo = codigo;
            p.proceso = proceso;
            p.acceso = acceso;
            p.tipoExp = tipo;
            p.nombre = nombre;

            ultcomp.liga = p;
            ultcomp = p;

            JOptionPane.showMessageDialog(null, "El articulo fue registrado con exito, puede continuar el proceso");
            

            pestanhas(true, true, true);
            camposComponentesOk();
        }
    }//GEN-LAST:event_btnRegiscompActionPerformed

