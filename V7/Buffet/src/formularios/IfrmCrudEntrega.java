    private void cargarDatos() {
        try {
            File f = new File("E:/componentes.in");
            BufferedReader entrada;
            entrada = new BufferedReader(new FileReader(f));
            String[] nodos = entrada.readLine().split("---"), valores;

            estReparacion p;

            for (int i = 0; i < nodos.length; i++) {
                p = new estReparacion();

                if (p == null) {
                    System.out.println("Memoria llena");
                } else {
                    valores = nodos[i].split(",");

                    p.usuario = valores[0];
                    p.tecnico = valores[1];
                    p.fecha = valores[2];
                    p.estado = valores[3];
                    p.comentarios = valores[4];
                    p.chequeo = valores[5];
                    p.articulo= valores[6];
                    p.adminIng= valores[7];
                    
                    if (cab == null) {
                        cab = p;
                    } else {
                        ult.liga = p;
                    }
                    ult = p;
                }
            }
        } catch (IOException e) {
            System.out.println("Error: " + e);
        }
    }
    
    private estReparacion consultarComponente(String componente) {
        estReparacion p = cab;

        while (p != null) {
            if (p.usuario.equals(componente)) {
                return p;
            }
            p = p.liga;
        }
        return null;
    }

    

    
