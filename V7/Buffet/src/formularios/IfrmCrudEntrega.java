    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        String usuario = txtCodigo.getText();
        String nombre = txtNombre.getText();
        String tipo = txtTipo.getText();
        String ubicacion = txtUbicacion.getText();
        String cantidad = txtCantidad.getText();
        String descripcion = txtDescripcion.getText();

        if (nombre.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta  llenar el campo de nombre", "Atención", JOptionPane.ERROR_MESSAGE);
            txtNombre.requestFocus();
        } else if (tipo.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta  llenar el campo de tipo", "Atención", JOptionPane.ERROR_MESSAGE);
            txtTipo.requestFocus();
        } else if (ubicacion.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta  llenar dar la ubicación", "Atención", JOptionPane.ERROR_MESSAGE);
            txtUbicacion.requestFocus();
        } else if (cantidad.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta  llenar el campo de cantidad", "Atención", JOptionPane.ERROR_MESSAGE);
            txtCantidad.requestFocus();
        } else {

            

            String miFila[] = {usuario, nombre, tipo, ubicacion, cantidad};

            DefaultTableModel miTabla = (DefaultTableModel) this.tblComponentes.getModel();
            miTabla.removeRow(tblComponentes.getSelectedRow());
            miTabla.addRow(miFila);

            estReparacion p = consultarComponente(usuario);

            p.usuario = usuario;
            p. tecnico= tipo;
            p.fecha = ubicacion;
            p.estado = descripcion;
            p.comentarios = cantidad;

            
            JOptionPane.showMessageDialog(null, "El componente fue modificado con exito");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

