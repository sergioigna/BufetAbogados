    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed

        String componentes = "";
        String reporte = txtReporte.getText();
        String estado = (String) cmbFinal.getSelectedItem();

        for (int i = 0; i < tblCompoAnha.getRowCount(); i++){
            componentes += (String) tblCompoAnha.getValueAt(i, 0) + " " + String.valueOf((Integer) tblCompoAnha.getValueAt(i, 4)) + " ";
        }

        if (componentes.equals("")){
            int option = JOptionPane.showConfirmDialog(rootPane, "No ingresó registro de componentes, ¿desea continuar sin agregarlos?");

            if(option == JOptionPane.YES_OPTION){
                finalizarReparacion(reporte, estado, componentes);
            }
            else if(option == JOptionPane.NO_OPTION){
                tbpSolicitud.setSelectedIndex(4);
            }
        }
        else {
            finalizarReparacion(reporte, estado, componentes);
            tablaCompo();
        }

    }//GEN-LAST:event_btnFinalizarActionPerformed

