    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String numExistencia = (String) tblComponentes.getValueAt(Integer.parseInt(lbId.getText()), 4);
            txtFiltrador.setText((String) tblComponentes.getValueAt(Integer.parseInt(lbId.getText()), 1));

            try {
                int cantidad = Integer.parseInt(txtCantidad.getText());

                if (cantidad == 0){
                    JOptionPane.showMessageDialog(null, "Ingrese una cantidad necesaria para la reparación");
                    txtCantidad.requestFocus();
                }
                else if (Integer.parseInt(numExistencia) < cantidad){
                    JOptionPane.showMessageDialog(null, "Ingrese una cantidad igual o menor a la existencia del componente");
                    txtCantidad.requestFocus();
                }
                else {
                    Object[] fila = new Object[5];

                    for (int i = 0; i < 4; i++){
                        fila[i] = tblComponentes.getValueAt(tblComponentes.getSelectedRow(), i);
                    }

                    int sw[] = comprobar(fila[0]);

                    if (sw[0] == 0){

                        fila[4] = cantidad;

                        String valorFinal = String.valueOf(Integer.parseInt(numExistencia) - cantidad);
                        tblComponentes.setValueAt(valorFinal, tblComponentes.getSelectedRow(), 4);

                        DefaultTableModel miModelo = (DefaultTableModel)this.tblCompoAnha.getModel();
                        miModelo.addRow(fila);

                        txtFiltrador.setText("");
                        txtCantidad.setText("");
                        lbId.setText("");
                    }
                    else {
                        JOptionPane.showMessageDialog(null, "El componente ya fue añadido");
                    }

                }
            }
            catch (NumberFormatException | HeadlessException ev){
                JOptionPane.showMessageDialog(null, "Ingrese la cantidad necesitada para la reparación");
                txtCantidad.requestFocus();
            }
        }
        catch (NumberFormatException | HeadlessException ev){
            JOptionPane.showMessageDialog(null, "Seleccione el componente que va a añadir");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

