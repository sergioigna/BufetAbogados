    private String fecha ()
    {
        Calendar Calendario = Calendar.getInstance();
       
        String anho = Integer.toString(Calendario.get(Calendar.YEAR));
        String mes = Integer.toString(Calendario.get(Calendar.MONTH) + 1);
        String dia = Integer.toString(Calendario.get(Calendar.DATE));
        
        return (dia + "/" + mes + "/" + anho);
    }
        
    private int[] comprobar (Object val)
    {
        int i;
        int sw[] = new int [2];
        
        for (i = 0; i < tblCompoAnha.getRowCount(); i++){
            if (tblCompoAnha.getValueAt(i, 0) == val){
                sw[0] = 1;
                sw[1] = i;
            }
        }
        return sw;
    }
    
        private int comprobarFinal (Object val)
    {
        int i;
        int sw = 0;
        
        for (i = 0; i < tblComponentes.getRowCount(); i++){
            if (tblComponentes.getValueAt(i, 0) == val){
                sw = i;
            }
        }
        return sw;
    }
    
    private void finalizarReparacion (String reporte, String estado, String componentes){
        
        if (reporte.equals("")){
           JOptionPane.showMessageDialog(null, "Debe dar el reporte de la reparación", "Atención", JOptionPane.ERROR_MESSAGE);
           txtReporte.requestFocus();
        }
        else if (cmbFinal.getSelectedIndex() == 0){
           JOptionPane.showMessageDialog(null, "Debe seleccionar el estado final de la reparación", "Atención", JOptionPane.ERROR_MESSAGE);
           cmbFinal.requestFocus();
        }
        else {
            String cod = String.valueOf((Integer) tblSolicitudes.getValueAt(codigo, 0));
            
            String miReparacion[] = {cod, reporte, estado, componentes, fecha()};
            
            sql.finalizarReparacion(miReparacion);
            
            DefaultTableModel miModelo = (DefaultTableModel)this.tblSolicitudes.getModel();
            miModelo.removeRow(codigo);
            
            JOptionPane.showMessageDialog(null, "La reparación se finalizó con exito");
            tbpSolicitud.setSelectedIndex(0);
            pestanhas(true, false);
        }
    }
        
