    private void btnDocumento1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocumento1ActionPerformed

        codigo = tblSolicitudes.getSelectedRow();
        String cod = String.valueOf(tblSolicitudes.getValueAt(codigo, 0));

        String[] datos = sql.consultarReparaciones(cod);

        if (cod.equals("")){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro de la tabla", "Atención", JOptionPane.ERROR_MESSAGE);
            tblSolicitudes.requestFocus();
        }
        else
        if (datos != null){

            txtDocSolicitante.setText(datos[0]);
            btnDocumentoActionPerformed(evt);
            txtNumSerie.setText(datos[1]);
            btnNumSerieActionPerformed(evt);
            txtComentarios.setText(datos[3]);
            JOptionPane.showMessageDialog(null, "La solicitud se ha cargado , puede continuar el proceso");
            pestanhas(true, true);
            tbpSolicitud.setSelectedIndex(1);

            int lista = 1;

            if (datos[2].length() == 21){
                lista = 0;
            }

            llenarTabla(lista, datos[2]);

        }

    }//GEN-LAST:event_btnDocumento1ActionPerformed

