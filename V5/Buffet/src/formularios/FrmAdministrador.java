    private void iconUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_iconUsuarioMouseClicked
        
        iconosGrises();
        iconUsuario.setIcon(new ImageIcon("src/imagenes/usuarios.png"));
        
        if(estacerrado(usuarios)){
            
            usuarios = new IfrmCrudUsuarios();
            jDesktopPane1.add(usuarios);
            
            BasicInternalFrameTitlePane titlePane = (BasicInternalFrameTitlePane)((BasicInternalFrameUI) usuarios.getUI()).getNorthPane();  
            usuarios.remove(titlePane); 
           
            usuarios.show();
        }
        else{
            try {
                usuarios.setSelected(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(FrmAdministrador.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
    }//GEN-LAST:event_iconUsuarioMouseClicked

