    public boolean estacerrado(Object obj){
        JInternalFrame[] activos  = jDesktopPane1.getAllFrames();
        boolean cerrado=true;
        int i = 0;
        
        while (i<activos.length && cerrado){
            if(activos[i]==obj){
                    cerrado=false;
            }
            i++;
        }
        return cerrado;
    }    

    private void cargarDatos() {
        try {
            File f = new File("D:/usuarios.in");
            BufferedReader entrada;
            entrada = new BufferedReader(new FileReader(f));
            String[] nodos = entrada.readLine().split("---"), valores;

            estUsuario p;

            for (int i = 0; i < nodos.length; i++) {
                p = new estUsuario();

                if (p == null) {
                    System.out.println("Memoria llena");
                } else {
                    valores = nodos[i].split(",");

                    p.usuario = valores[0];
                    p.contrasena = valores[1];
                    p.nombre = valores[2];
                    p.apellido = valores[3];
                    p.email = valores[4];
                   // p.departamento = valores[5];
                    p.perfil = valores[5];
                    p.estado = valores[6];
                    p.fecha = valores[7];

                    if (cab == null) {
                        cab = p;
                    } else {
                        ult.liga = p;
                    }
                    ult = p;
                }
            }
        } catch (IOException e) {
            System.out.println("Error: " + e);
        }
    }

    private estUsuario consultarUsuario(String usuario) {
        estUsuario p = cab;

        while (p != null) {
            if (p.usuario.equals(usuario)) {
                return p;
            }
            p = p.liga;
        }
        return null;
    }
    
